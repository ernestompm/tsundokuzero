## Fortalezas y posibles críticas

Su mayor virtud es la capacidad de hacer accesibles asuntos difíciles. Presenta la depresión, el miedo a equivocarse o la muerte sin utilizar un lenguaje excesivamente clínico ni una estructura compleja. Sus libros resultan acogedores y suelen generar identificación y conversación.

La otra cara es que algunos lectores encuentran su mensaje demasiado explícito, sentimental o didáctico. Como sus historias suelen tener una enseñanza reconocible, pueden parecer previsibles o cercanas a la autoayuda disfrazada de ficción. El propio Haig reconoce que su «filosofía popular» y su vertiente de consuelo provocan reacciones muy polarizadas.

